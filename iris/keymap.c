bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LEFT_MOD:
      if (record->event.pressed) {
        layer_on(_LAYER_LEFT_MODIFIER);
      } else {
        layer_off(_LAYER_LEFT_MODIFIER);
      }
      return false;
      break;
    case RIGHT_MOD:
      if (record->event.pressed) {
        layer_on(_LAYER_LEFT_MODIFIER);
        layer_on(_LAYER_RIGHT_MODIFIER);
      } else {
        layer_off(_LAYER_LEFT_MODIFIER);
        layer_off(_LAYER_RIGHT_MODIFIER);
      }
      return false;
      break;
  }
  return true;
}

bool encoder_update_user(uint8_t index, bool clockwise) {
  if (index == 1) {
    if (clockwise) {
      tap_code(KC_VOLU);
    } else {
      tap_code(KC_VOLD);
    }
  }
  return true;
}
